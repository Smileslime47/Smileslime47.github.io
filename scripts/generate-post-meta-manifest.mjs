import { readdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'

const ROOT = process.cwd()
const POSTS_DIR = path.join(ROOT, 'src', 'posts')
const OUTPUT_FILE = path.join(ROOT, 'src', 'service', 'posts', 'meta-manifest.generated.ts')

async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true })
  const files = await Promise.all(
    entries.map(async (entry) => {
      const full = path.join(dir, entry.name)
      if (entry.isDirectory()) return walk(full)
      if (entry.isFile() && entry.name.toLowerCase().endsWith('.md')) return [full]
      return []
    })
  )
  return files.flat()
}

function parseFrontmatter(raw) {
  if (!raw.startsWith('---')) return {}
  const end = raw.indexOf('\n---', 3)
  if (end < 0) return {}
  const block = raw.slice(3, end).replace(/^\r?\n/, '')
  const lines = block.split(/\r?\n/)
  const frontmatter = {}
  let currentKey = ''

  for (const line of lines) {
    const arrayItem = line.match(/^\s*-\s*(.+)\s*$/)
    if (arrayItem && currentKey) {
      const list = Array.isArray(frontmatter[currentKey]) ? frontmatter[currentKey] : []
      list.push(arrayItem[1].trim())
      frontmatter[currentKey] = list
      continue
    }

    const pair = line.match(/^([A-Za-z0-9_-]+)\s*:\s*(.*)$/)
    if (!pair) continue

    const key = pair[1]
    const value = pair[2].trim()
    currentKey = key
    if (value === '') {
      frontmatter[key] = []
    } else {
      frontmatter[key] = value
    }
  }

  return frontmatter
}

function normalizeDate(frontmatter) {
  const keys = ['date', 'publishedAt', 'publishDate', 'createdAt']
  for (const key of keys) {
    const raw = frontmatter[key]
    if (typeof raw === 'string' && raw.trim()) return raw.trim()
  }
  return undefined
}

function toId(file) {
  return path.relative(POSTS_DIR, file).split(path.sep).join('/')
}

async function main() {
  const files = await walk(POSTS_DIR)
  const manifest = {}

  for (const file of files) {
    const raw = await readFile(file, 'utf8')
    const frontmatter = parseFrontmatter(raw)
    const publishedAt = normalizeDate(frontmatter)
    manifest[toId(file)] = {
      frontmatter,
      publishedAt,
    }
  }

  const content =
    `/* eslint-disable */\n` +
    `// Auto-generated by scripts/generate-post-meta-manifest.mjs\n` +
    `import type { FrontmatterValue } from './types'\n\n` +
    `export type PostMetaManifestEntry = {\n` +
    `  frontmatter: Record<string, FrontmatterValue>\n` +
    `  publishedAt?: string\n` +
    `}\n\n` +
    `export const postMetaManifest: Record<string, PostMetaManifestEntry> = ${JSON.stringify(manifest, null, 2)}\n`

  await writeFile(OUTPUT_FILE, content, 'utf8')
  console.log(`Generated ${OUTPUT_FILE}`)
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
